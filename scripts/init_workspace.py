def main():
    target = sys.argv[1] if len(sys.argv) > 1 else "."
    target = os.path.abspath(target)
    os.makedirs(target, exist_ok=True)

    created, skipped = [], []

    for d in DIRS:
        path = os.path.join(target, d)
        if os.path.isdir(path):
            skipped.append(d + "/")
        else:
            os.makedirs(path)
            created.append(d + "/")

    for d in GITKEEP_DIRS:
        gitkeep = os.path.join(target, d, ".gitkeep")
        if not os.path.exists(gitkeep):
            with open(gitkeep, "w", encoding="utf-8"):
                pass

    for rel, content in FILES.items():
        path = os.path.join(target, rel)
        if os.path.exists(path):
            skipped.append(rel)
        else:
            parent = os.path.dirname(path)
            if parent:
                os.makedirs(parent, exist_ok=True)
            with open(path, "w", encoding="utf-8") as f:
                f.write(content)
            created.append(rel)

    print("=== 専属リサーチャー 作業フォルダ ===")
    print("場所: %s" % target)
    print()
    if created:
        print("作成:")
        for c in created:
            print("  + %s" % c)
    if skipped:
        print("スキップ（既存のため変更なし）:")
        for s in skipped:
            print("  = %s" % s)
    print()
    print("次の一歩: 自社サービス.md を記入 → /competitor-researcher:research")


if __name__ == "__main__":
    main()
